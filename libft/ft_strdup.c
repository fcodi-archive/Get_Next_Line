#include "includes/libft.h"

char	*ft_strdup(const char *s1)
{
	char	*result;

	if ((result = (char *)malloc(sizeof(char) * (ft_strlen((char *)s1) + 1))))
		result = ft_strcpy(result, s1);
	return (result);
}
